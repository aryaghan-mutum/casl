(ns clojure-functions.strings)

;(require '[clojure.string :as str])

(clojure.string/capitalize "fgjhdfg fdgfdkgh")              ;"Fgjhdfg fdgfdkgh"

(clojure.string/blank? "")                                  ;true

(clojure.string/capitalize "clojure")                       ;Clojure

(clojure.string/upper-case "clojure")                       ;CLOJURE

(clojure.string/lower-case "CLOJURE")                       ;clojure
(clojure.string/lower-case "!%$#&^")                        ;"!%$#&^"








